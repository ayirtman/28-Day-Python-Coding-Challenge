# Day 1 Bonus Challenge: Greeting User

# Ask for the user's name
name = input("Please enter your name: ")

# Ask for the user's age
age = input("Please enter your age: ")

# Ask for the user's favorite color
color = input("Please enter your favorite color: ")

# Bonus: Ask for the user's country of residence
country = input("Please enter your country of residence: ")

# Bonus: Ask for the user's favorite food
food = input("Please enter your favorite food: ")

# Print a greeting that includes the user's details
print(f"Hello, {name}! You are {age} years old and your favorite color is {color}.")
print(f"You live in {country} and your favorite food is {food}.")